/*
그럼 배열을 왜 사용하지 않고 리터럴을 사용하는걸까? 
가장 큰 차이점은 생김새
객체 리터럴은 속성이 있고, 속성의이름, 속성의값들이 있다.
배열은 이름이 안붙어있고, 인덱스만 있다.

이름이 필요하다 > 객체 리터럴
이름이 필요없다 > 배열
*/